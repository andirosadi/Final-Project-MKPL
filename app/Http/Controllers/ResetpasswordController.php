    public function update(Request $request, $id)
    {
      $user = User::find($id);
      $this->validate(request(),[
        'password'=>'required|min:6',
      ]);
      $user->password = bcrypt($request->password);
      $user->update();
      return redirect('admin')->with('message', 'User berhasil di update');
